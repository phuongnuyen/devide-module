extension Sequence {
    
    public func getCGColors() -> [CGColor] {
        var cgColors = [CGColor]()
        for item in self {
            guard let color = item as? UIColor else {
                return cgColors
            }
            cgColors.append(color.cgColor)
        }
        return cgColors
    }
    
}


extension UIImageView {
    public func setImageColor(color: UIColor) {
        let templateImage = self.image?.withRenderingMode(.alwaysTemplate)
        self.image = templateImage
        self.tintColor = color
    }
}

extension UIView {
    public func dropShadow() {
        layer.cornerRadius = 8
        layer.shadowColor = UIColor.Other.overlay.cgColor
        layer.shadowOpacity = 0.1
        layer.shadowRadius = 12.0
        layer.masksToBounds = false
        layer.borderWidth = 1
        layer.borderColor = UIColor.Other.stroke.cgColor
        layer.shadowOffset = CGSize(width: 0, height: 2.0)
    }
